        <p>Generally you're gonna want to maximize your return customers, you'll be doing that by making their experience as smooth and pleasant as possible. Here's some tips to help you maximize retention.</p>
        
        <p><strong>Assume users are idiots. </strong>Simplest way to build around the inevitable headaches they will run into is by making everything as easy and 
            straightforward as possible. Stick to a clean UI preventing them from getting lost.</p>
        <p><strong>Repetition makes the dream work. </strong>Repeat common tropes so patterns get ingrained into our users making their experience more intuitive;
            the less they have to think about what they're doing the more time they'll spend on your content. Think of how you would want to traverse a large website,
            it would be nice to know it's organized in a standard way, be it by implementing directions within a hamburger or always on a side panel. However you choose to organize,
            stick with your common patterns.</p>
        
        <h4>tl;dr:</h4>
        <ol>
            <li>Assume users are idiots</li>
            <li>Repetition makes the dream work</li>
